package org.nowitzki.smashboy.utils;

public class Vector2 {
	public int X;
	public int Y;
	
	public Vector2() {
		
	}
	
	public Vector2(int x, int y) {
		this.X = x;
		this.Y = y;
	}
	
	public int getX() {
		return X;
	}
	
	public void setX(int x) {
		this.X = x;
	}
	
	public int getY() {
		return Y;
	}
	
	public void setY(int y) {
		this.Y = y;
	}
}
